#include <stdio.h>
#include <stdint.h>
#include "submodule/lib_cpp/lib_cpp.h"
//-----------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------------//
// general function
int main(int argc, char* argv[])
{
	if (argc != 1)
	{
		printf("%s\t(%s)\n", PROG_FULL_NAME, PROG_URL);
		printf("example: echo \"68656c6c6f206675636b696e6720776f726c64210a\" | %s\n", argv[0]);
		printf("\n");
		return 1;
	}


	int ch1, ch2;
	uint8_t r1, r2, out;


// read from stdin, convert to bin, write to stdout
	for (;;)
	{
		ch1 = getchar();
		if (ch1 == EOF) break;

		ch2 = getchar();
		if (ch2 == EOF) break;

		if (lib_cpp::hex2bin((uint8_t)ch1, r1) == false) break;
		if (lib_cpp::hex2bin((uint8_t)ch2, r2) == false) break;

		out = (uint8_t)((r1 << 4) + r2);

		putchar(out);
	}
	fflush(stdout);


	return 0;
}
