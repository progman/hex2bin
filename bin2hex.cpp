#include <stdio.h>
#include <stdlib.h>
//-----------------------------------------------------------------------------------------------------------------------------------------------------------//
// general function
int main(int argc, char* argv[])
{
	if (argc != 1)
	{
		printf ("example: echo \"hello world\" | %s\n", argv[0]);
		exit(1);
	}


// read from stdin, convert to hex, write to stdout
	for(;;)
	{
		int ch = getchar();
		if (ch == EOF) break;
		printf ("%02X", (unsigned char)(ch));
	}
	fflush(stdout);


	return 0;
}
